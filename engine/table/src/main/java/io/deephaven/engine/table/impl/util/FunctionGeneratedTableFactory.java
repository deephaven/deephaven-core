package io.deephaven.engine.table.impl.util;

import io.deephaven.engine.context.ExecutionContext;
import io.deephaven.engine.rowset.*;
import io.deephaven.engine.table.*;
import io.deephaven.engine.table.impl.ListenerRecorder;
import io.deephaven.engine.table.impl.MergedListener;
import io.deephaven.engine.table.impl.QueryTable;
import io.deephaven.engine.table.impl.TableUpdateImpl;
import io.deephaven.engine.table.impl.sources.ArrayBackedColumnSource;
import io.deephaven.engine.table.impl.sources.NullValueColumnSource;
import io.deephaven.engine.table.impl.sources.SwitchColumnSource;
import io.deephaven.engine.updategraph.NotificationQueue;
import io.deephaven.engine.updategraph.UpdateGraph;
import io.deephaven.util.SafeCloseable;
import io.deephaven.util.annotations.ReferentialIntegrity;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

import javax.annotation.OverridingMethodsMustInvokeSuper;
import java.time.Duration;
import java.util.*;
import java.util.function.Supplier;
import java.util.stream.Collectors;

/**
 * An abstract table that represents the result of a function.
 * <p>
 * The table will run by regenerating the full values (using the tableGenerator Function passed in). The resultant
 * table's values are copied into the result table and appropriate listener notifications are fired.
 * <p>
 * When copying data (the default), all the rows in the output table are modified on every tick, even if no actual
 * changes occurred, and the output table has a contiguous RowSet. When copying is disabled (see
 * {@link FunctionGeneratedTableSpec#copyData()}), the output instead delegates to the generated table's column sources
 * via {@link SwitchColumnSource}, adopting the generated table's RowSet and firing a full-replacement update (all
 * previous rows removed, all new rows added) each cycle.
 * <p>
 * When {@link FunctionGeneratedTableSpec#blinkTable()} is set, the result is presented as a
 * {@link Table#BLINK_TABLE_ATTRIBUTE blink table}: each cycle removes the previous rows and adds the newly generated
 * rows (also a full replacement), and a cycle that produces no rows clears the result.
 * <p>
 * The generator function must produce a V2 table, and the table definition must not change between invocations.
 * <p>
 * If you are transforming a table, you should generally prefer to use the regular table operations as opposed to this
 * factory, because they are capable of performing some operations incrementally. However, for small tables this might
 * prove to require less development effort.
 */
public class FunctionGeneratedTableFactory {

    /**
     * The normalized generator. An empty {@link Optional} indicates that the previous result should be retained (only
     * possible with a {@link FunctionGeneratedTableSpec#retainingLastTableSupplier()}).
     */
    private final Supplier<Optional<Table>> tableGenerator;
    private final int refreshIntervalMs;
    private final boolean copyData;
    private final boolean blink;

    /** Populated only when {@link #copyData} is {@code true}. */
    private final Map<String, WritableColumnSource<?>> writableSources = new LinkedHashMap<>();
    /** Populated only when {@link #copyData} is {@code false}. */
    private final Map<String, SwitchColumnSource<?>> switchSources = new LinkedHashMap<>();
    private final Map<String, ColumnSource<?>> columns = new LinkedHashMap<>();
    private final TrackingWritableRowSet rowSet;
    private final ExecutionContext executionContextForUpdates;

    private long nextRefresh;

    /**
     * Create a table that refreshes based on the value of your function, automatically called every refreshIntervalMs.
     *
     * @param tableGenerator a function returning a table to copy into the output table
     * @return a ticking table (assuming refreshIntervalMs is &gt; 0) generated by tableGenerator
     */
    public static Table create(@NotNull final Supplier<Table> tableGenerator, final int refreshIntervalMs) {
        final FunctionGeneratedTableSpec.Builder builder = FunctionGeneratedTableSpec.builder()
                .tableSupplier(tableGenerator);
        if (refreshIntervalMs > 0) {
            builder.refreshInterval(Duration.ofMillis(refreshIntervalMs));
        }
        return create(builder.build());
    }

    /**
     * Create a table that refreshes based on the value of your function, automatically called in a
     * dependency-respecting way when at least one of the {@code sourceTables} tick.
     * <p>
     * <em>Note</em> that the {@code tableGenerator} may access data in the {@code sourceTables} but should not perform
     * further table operations on them without careful handling. Table operations may be memoized, and it is possible
     * that a table operation will return a table created by a previous invocation of the same operation. Since that
     * result will not have been included in the {@code sourceTables}, it's not automatically treated as a dependency
     * for purposes of determining when it's safe to invoke {@code tableGenerator}, allowing races to exist between
     * accessing the operation result and that result's own update processing. It's best to include all dependencies
     * directly in {@code sourceTables}, or only compute on-demand inputs under a
     * {@link io.deephaven.engine.liveness.LivenessScope}.
     *
     * @param tableGenerator a function returning a table to copy into the output table
     * @param sourceTables The query engine does not know the details of your function inputs. If you are dependent on a
     *        ticking table tables in your tableGenerator function, you can add it to this list so that the function
     *        will be recomputed on each tick.
     * @return a ticking table (assuming sourceTables have been specified) generated by tableGenerator
     */
    public static Table create(@NotNull final Supplier<Table> tableGenerator, @NotNull final Table... sourceTables) {
        // A zero-length sourceTables produces a spec with no dependencies, i.e. a run-once static result.
        return create(FunctionGeneratedTableSpec.builder()
                .tableSupplier(tableGenerator)
                .addDependencies(sourceTables)
                .build());
    }

    /**
     * Create a table according to the supplied {@link FunctionGeneratedTableSpec}.
     *
     * @param spec the specification describing how the table is generated and refreshed
     * @return a table generated by the spec's supplier
     */
    public static Table create(@NotNull final FunctionGeneratedTableSpec spec) {
        if (!spec.dependencies().isEmpty()) {
            return createWithDependencies(spec);
        }
        return new FunctionGeneratedTableFactory(spec).getTable();
    }

    /**
     * Wrap an always-producing supplier into the normalized retaining-last supplier form, where every invocation
     * produces a (present) table.
     */
    private static Supplier<Optional<Table>> alwaysSupplier(@NotNull final Supplier<Table> tableGenerator) {
        return () -> Optional.of(tableGenerator.get());
    }

    /**
     * Wire the source-table (dependency) driven refresh for a spec that specifies
     * {@link FunctionGeneratedTableSpec#dependencies()}.
     */
    private static FunctionBackedTable createWithDependencies(@NotNull final FunctionGeneratedTableSpec spec) {
        final Table[] sourceTables = spec.dependencies().toArray(Table[]::new);

        // Verify the source tables are all refreshing.
        final Collection<Table> notRefreshing =
                Arrays.stream(sourceTables).filter(t -> !t.isRefreshing()).collect(Collectors.toList());
        if (!notRefreshing.isEmpty()) {
            throw new IllegalArgumentException("All source tables must be refreshing: " + notRefreshing);
        }

        final UpdateGraph updateGraph = NotificationQueue.Dependency.getUpdateGraph(sourceTables[0], sourceTables);
        final FunctionBackedTable functionBackedTableResult;
        try (final SafeCloseable ignored = ExecutionContext.getContext().withUpdateGraph(updateGraph).open()) {
            final FunctionGeneratedTableFactory factory = new FunctionGeneratedTableFactory(spec);
            functionBackedTableResult = factory.getTable();
            functionBackedTableResult.getUpdateGraph().checkInitiateSerialTableOperation();

            final List<ListenerRecorder> listenerRecorders = new ArrayList<>(sourceTables.length);
            for (int ii = 0; ii < sourceTables.length; ii++) {
                Table sourceTable = sourceTables[ii];
                listenerRecorders.add(
                        new ListenerRecorder("FunctionGeneratedTable_source_" + ii, sourceTable, null));
            }

            final MergedListener mergedListener = new MergedListener(
                    listenerRecorders,
                    Collections.emptyList(),
                    "FunctionGeneratedTableFactory",
                    functionBackedTableResult) {
                @Override
                protected void process() {
                    functionBackedTableResult.doRefresh();
                }
            };

            for (int ii = 0; ii < listenerRecorders.size(); ii++) {
                ListenerRecorder listenerRecorder = listenerRecorders.get(ii);
                listenerRecorder.setMergedListener(mergedListener);
                sourceTables[ii].addUpdateListener(listenerRecorder);
            }

            functionBackedTableResult.setParentListener(mergedListener);
        }

        return functionBackedTableResult;
    }

    private FunctionGeneratedTableFactory(@NotNull final FunctionGeneratedTableSpec spec) {
        this.tableGenerator = spec.tableSupplier()
                .map(FunctionGeneratedTableFactory::alwaysSupplier)
                .orElseGet(() -> spec.retainingLastTableSupplier().orElseThrow());
        this.copyData = spec.copyData();
        this.blink = spec.blinkTable();
        // A specified interval drives periodic refreshes; dependencies drive a merged-listener refresh (interval 0, no
        // registered source); neither means a run-once static result (interval -1).
        this.refreshIntervalMs = spec.refreshInterval()
                .map(interval -> Math.toIntExact(interval.toMillis()))
                .orElseGet(() -> spec.dependencies().isEmpty() ? -1 : 0);
        final TableDefinition definitionOverride = spec.tableDefinition().orElse(null);
        this.executionContextForUpdates = makeExecutionContextForUpdates();
        nextRefresh = System.currentTimeMillis() + this.refreshIntervalMs;

        final Table initialTable;
        try (final SafeCloseable ignored = executionContextForUpdates.open()) {
            initialTable = tableGenerator.get().orElse(null);
        }
        if (initialTable != null && initialTable.isRefreshing()) {
            if (ExecutionContext.getContext().getUpdateGraph() != initialTable.getUpdateGraph()) {
                throw new IllegalStateException(
                        "Function-generated tables must belong to the same UpdateGraph as the creating FunctionGeneratedTableFactory.");
            }
            initialTable.getUpdateGraph().checkInitiateSerialTableOperation();
        }

        // Determine the result definition; when the (retaining-last) supplier produced nothing, we need an explicit
        // definition to describe the columns.
        final TableDefinition definition;
        if (initialTable != null) {
            definition = initialTable.getDefinition();
        } else if (definitionOverride != null) {
            definition = definitionOverride;
        } else {
            throw new IllegalStateException(
                    "The function-generated table's supplier produced no initial table and no table definition was provided.");
        }

        if (copyData) {
            for (final ColumnDefinition<?> columnDefinition : definition.getColumns()) {
                final WritableColumnSource<?> memoryColumnSource = ArrayBackedColumnSource.getMemoryColumnSource(
                        0, columnDefinition.getDataType(), columnDefinition.getComponentType());
                columns.put(columnDefinition.getName(), memoryColumnSource);
                writableSources.put(columnDefinition.getName(), memoryColumnSource);
            }

            final long initialSize;
            if (initialTable != null) {
                copyTable(initialTable);
                initialSize = initialTable.size();
            } else {
                initialSize = 0;
            }

            // enable prev tracking after columns are initialized
            columns.values().forEach(ColumnSource::startTrackingPrevValues);

            rowSet = RowSetFactory.flat(initialSize).toTracking();
        } else {
            if (initialTable != null) {
                for (final Map.Entry<String, ? extends ColumnSource<?>> entry : initialTable.getColumnSourceMap()
                        .entrySet()) {
                    // noinspection unchecked
                    final SwitchColumnSource<?> switchSource =
                            new SwitchColumnSource<>((ColumnSource<Object>) entry.getValue());
                    columns.put(entry.getKey(), switchSource);
                    switchSources.put(entry.getKey(), switchSource);
                }
                rowSet = initialTable.getRowSet().copy().toTracking();
            } else {
                for (final ColumnDefinition<?> columnDefinition : definition.getColumns()) {
                    final ColumnSource<?> nullSource = NullValueColumnSource.getInstance(
                            columnDefinition.getDataType(), columnDefinition.getComponentType());
                    // noinspection unchecked
                    final SwitchColumnSource<?> switchSource =
                            new SwitchColumnSource<>((ColumnSource<Object>) nullSource);
                    columns.put(columnDefinition.getName(), switchSource);
                    switchSources.put(columnDefinition.getName(), switchSource);
                }
                rowSet = RowSetFactory.empty().toTracking();
            }
        }
    }

    /**
     * If we have a systemic context we need to capture the authentication context. If we have a user-supplied context
     * we should keep that so that any query scope, query library, etc. is available to produce the table.
     *
     * @return the execution context to use for updates
     */
    private static @NotNull ExecutionContext makeExecutionContextForUpdates() {
        final ExecutionContext contextToRecord = ExecutionContext.getContextToRecord();
        if (contextToRecord != null) {
            return contextToRecord;
        } else {
            return ExecutionContext.newBuilder().build();
        }
    }

    private FunctionBackedTable getTable() {
        return new FunctionBackedTable(rowSet, columns);
    }

    /**
     * Invoke the generator and validate the result. Returns an empty {@link Optional} when the retaining-last supplier
     * declined to produce a new table.
     */
    private Optional<Table> generate() {
        final Optional<Table> generated;
        try (final SafeCloseable ignored = executionContextForUpdates.open()) {
            generated = tableGenerator.get();
        }
        generated.ifPresent(newTable -> {
            if (newTable.isRefreshing()) {
                if (ExecutionContext.getContext().getUpdateGraph() != newTable.getUpdateGraph()) {
                    throw new IllegalStateException(
                            "Function-generated tables must belong to the same UpdateGraph as the creating FunctionGeneratedTableFactory.");
                }
                if (!newTable.getUpdateGraph().satisfied(newTable.getUpdateGraph().clock().currentStep())) {
                    throw new IllegalStateException(
                            "The function-generated table is not satisfied; did you miss a dependency when specifying source tables?");
                }
            }
        });
        return generated;
    }

    private void copyTable(Table source) {
        final Map<String, ? extends ColumnSource<?>> sourceColumns = source.getColumnSourceMap();
        final ChunkSource.WithPrev[] sourceColumnsArray = new ChunkSource.WithPrev[sourceColumns.size()];
        final WritableColumnSource[] destColumnsArray = new WritableColumnSource[sourceColumns.size()];

        final RowSet sourceRowSet = source.getRowSet();
        int cc = 0;
        for (Map.Entry<String, ? extends ColumnSource<?>> entry : sourceColumns.entrySet()) {
            WritableColumnSource<?> destColumn = writableSources.get(entry.getKey());
            destColumn.ensureCapacity(sourceRowSet.size());
            sourceColumnsArray[cc] = entry.getValue();
            destColumnsArray[cc++] = destColumn;
        }

        // noinspection unchecked
        ChunkUtils.copyData(sourceColumnsArray, sourceRowSet, destColumnsArray,
                RowSequenceFactory.forRange(0, sourceRowSet.size() - 1),
                false);
    }

    /**
     * @implNote The constructor publishes {@code this} to the {@link UpdateGraph} and cannot be subclassed.
     */
    private final class FunctionBackedTable extends QueryTable implements Runnable {

        private volatile MergedListener parentListener;

        @ReferentialIntegrity
        private Runnable delayedErrorReference;

        private FunctionBackedTable(
                @NotNull final TrackingRowSet rowSet,
                @NotNull final Map<String, ColumnSource<?>> columns) {
            super(rowSet, columns);
            if (refreshIntervalMs >= 0) {
                setRefreshing(true);
                if (refreshIntervalMs > 0) {
                    updateGraph.addSource(this);
                }
            }
            if (blink) {
                setAttribute(Table.BLINK_TABLE_ATTRIBUTE, Boolean.TRUE);
            }
        }

        private void setParentListener(@NotNull final MergedListener parentListener) {
            addParentReference(parentListener);
            // Keep an extra reference to our parent in case we need to force its reference count to zero before we
            // destroy ourselves.
            this.parentListener = parentListener;
        }

        @Override
        public void run() {
            if (System.currentTimeMillis() < nextRefresh) {
                return;
            }
            nextRefresh = System.currentTimeMillis() + refreshIntervalMs;

            doRefresh();
        }

        private void doRefresh() {
            try {
                final Optional<Table> generated = generate();
                if (generated.isEmpty()) {
                    // A retaining-last supplier declined to produce a new table; preserve the current result.
                    return;
                }
                if (copyData) {
                    doRefreshCopy(generated.get());
                } else {
                    doRefreshSwitch(generated.get());
                }
            } catch (Exception e) {
                // Remove this failed table from the update graph.
                if (refreshIntervalMs > 0) {
                    updateGraph.removeSource(this);
                }

                // Notify listeners that we had an issue refreshing the table.
                if (getLastNotificationStep() == updateGraph.clock().currentStep()) {
                    if (parentListener != null) {
                        parentListener.forceReferenceCountToZero();
                    }
                    delayedErrorReference = new DelayedErrorNotifier(e, null, this);
                } else {
                    notifyListenersOnError(e, null);
                    forceReferenceCountToZero();
                }
            }
        }

        /**
         * Copy the generated table into our flat, contiguous writable column sources and fire the resulting
         * notification. For a blink result the change is a full replacement (all previous rows removed, all new rows
         * added); otherwise surviving rows are reported as modified.
         */
        private void doRefreshCopy(@NotNull final Table newTable) {
            final long size = rowSet.size();
            copyTable(newTable);
            final long newSize = newTable.size();

            if (blink) {
                final WritableRowSet removed = RowSetFactory.flat(size);
                final WritableRowSet added = RowSetFactory.flat(newSize);
                rowSet.resetTo(added);
                if (added.isEmpty() && removed.isEmpty()) {
                    added.close();
                    removed.close();
                    return;
                }
                notifyListeners(new TableUpdateImpl(added, removed, RowSetFactory.empty(), RowSetShiftData.EMPTY,
                        ModifiedColumnSet.EMPTY));
                return;
            }

            if (newSize < size) {
                final RowSet removed = RowSetFactory.fromRange(newSize, size - 1);
                rowSet.remove(removed);
                final RowSet modified = rowSet.copy();
                notifyListeners(RowSetFactory.empty(), removed, modified);
                return;
            }
            if (newSize > size) {
                final RowSet added = RowSetFactory.fromRange(size, newSize - 1);
                final RowSet modified = rowSet.copy();
                rowSet.insert(added);
                notifyListeners(added, RowSetFactory.empty(), modified);
                return;
            }
            if (size > 0) {
                // no size change, just modified
                final RowSet modified = rowSet.copy();
                notifyListeners(RowSetFactory.empty(), RowSetFactory.empty(), modified);
            }
        }

        /**
         * Swap our {@link SwitchColumnSource}s onto the newly generated table's column sources, adopt its RowSet, and
         * fire the resulting update. Since the generator regenerates the full table every cycle, the change is modeled
         * as a full replacement: the previous rows are removed and the new rows are added, with no modifications. The
         * generated table is static, so retaining its column sources through the switch sources is sufficient; no
         * liveness management of the table itself is required.
         */
        private void doRefreshSwitch(@NotNull final Table newTable) {
            for (final Map.Entry<String, ? extends ColumnSource<?>> entry : newTable.getColumnSourceMap().entrySet()) {
                // noinspection unchecked,rawtypes
                ((SwitchColumnSource) switchSources.get(entry.getKey())).setNewCurrent(entry.getValue());
            }

            final WritableRowSet added = newTable.getRowSet().copy();
            final WritableRowSet removed = rowSet.copy();

            // Replacing the entire previous RowSet keeps the added rows disjoint from the post-removal RowSet, so the
            // added/removed key ranges may overlap freely without violating the update contract.
            rowSet.resetTo(newTable.getRowSet());

            if (added.isEmpty() && removed.isEmpty()) {
                added.close();
                removed.close();
                return;
            }
            notifyListeners(new TableUpdateImpl(added, removed, RowSetFactory.empty(), RowSetShiftData.EMPTY,
                    ModifiedColumnSet.EMPTY));
        }

        @OverridingMethodsMustInvokeSuper
        @Override
        public void destroy() {
            super.destroy();
            if (refreshIntervalMs > 0) {
                updateGraph.removeSource(this);
            }
            if (parentListener != null) {
                parentListener.forceReferenceCountToZero();
            }
        }
    }
}
